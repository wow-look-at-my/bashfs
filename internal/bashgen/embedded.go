package bashgen

import (
	"fmt"
	"os"
	"strings"

	"bashfs/internal/embed"
	"bashfs/internal/fswalker"
)

// OffsetPlaceholder is replaced with the actual byte offset at packaging time.
// It must be exactly 20 characters so the replacement preserves byte count.
const OffsetPlaceholder = "00000000000000000000"

// EmbeddedResult holds the generated bash code and the raw binary payload.
type EmbeddedResult struct {
	Script  string // bash code with offset map + helper functions
	Payload []byte // concatenated gzip-compressed file data
}

// GenerateEmbedded produces bash code that reads compressed file data from the
// end of the script itself (after an exit 0 guard). File contents are stored as
// a raw binary payload appended to the script; the bash code uses tail/head to
// extract individual files by byte offset and length.
func GenerateEmbedded(files []fswalker.FileEntry) (*EmbeddedResult, error) {
	var payload []byte
	type fileInfo struct {
		relPath string
		offset  int
		length  int
	}
	var infos []fileInfo

	for _, f := range files {
		data, err := os.ReadFile(f.AbsPath)
		if err != nil {
			return nil, fmt.Errorf("reading %s: %w", f.AbsPath, err)
		}
		compressed, err := embed.Compress(data)
		if err != nil {
			return nil, fmt.Errorf("compressing %s: %w", f.RelPath, err)
		}
		infos = append(infos, fileInfo{
			relPath: f.RelPath,
			offset:  len(payload),
			length:  len(compressed),
		})
		payload = append(payload, compressed...)
	}

	var b strings.Builder
	b.WriteString("# Generated by bashfs (embedded mode) - DO NOT EDIT\n\n")

	// Payload offset placeholder — replaced at packaging time with the real
	// 1-indexed byte position of the binary payload within the script file.
	fmt.Fprintf(&b, "__bashfs_payload_offset=%s\n", OffsetPlaceholder)
	b.WriteString("__bashfs_payload_offset=$((10#$__bashfs_payload_offset))\n\n")

	b.WriteString("declare -A __bashfs_offset=(\n")
	for _, info := range infos {
		fmt.Fprintf(&b, "  [\"%s\"]=\"%d:%d\"\n", info.relPath, info.offset, info.length)
	}
	b.WriteString(")\n")

	b.WriteString(`
bashfs_cat() {
  local _info="${__bashfs_offset[$1]}"
  if [ -z "$_info" ]; then
    echo "bashfs: file not found: $1" >&2
    return 1
  fi
  local _off="${_info%%:*}"
  local _len="${_info##*:}"
  tail -c "+$((__bashfs_payload_offset + _off))" "${BASH_SOURCE[0]}" | head -c "$_len" | gzip -d
}

bashfs_extract() {
  local _info="${__bashfs_offset[$1]}"
  if [ -z "$_info" ]; then
    echo "bashfs: file not found: $1" >&2
    return 1
  fi
  local _off="${_info%%:*}"
  local _len="${_info##*:}"
  mkdir -p "$(dirname "$2")" && tail -c "+$((__bashfs_payload_offset + _off))" "${BASH_SOURCE[0]}" | head -c "$_len" | gzip -d > "$2"
}

`)

	b.WriteString("bashfs_list() {\n")
	for _, info := range infos {
		fmt.Fprintf(&b, "  echo '%s'\n", info.relPath)
	}
	b.WriteString("}\n")

	b.WriteString("\nbashfs_jq() { bashfs_cat \"$1\" | jq \"${2:-.}\"; }\n")

	return &EmbeddedResult{
		Script:  b.String(),
		Payload: payload,
	}, nil
}
