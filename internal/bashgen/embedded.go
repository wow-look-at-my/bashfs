package bashgen

import (
	"fmt"
	"os"
	"strings"

	"bashfs/internal/embed"
	"bashfs/internal/fswalker"
)

// GenerateEmbedded produces self-contained bash code with all file contents
// gzip-compressed and base64-encoded in a declare -A associative array.
func GenerateEmbedded(files []fswalker.FileEntry) (string, error) {
	var b strings.Builder
	b.WriteString("# Generated by bashfs (embedded mode) - DO NOT EDIT\n\n")

	b.WriteString("declare -A __bashfs_data=(\n")
	for _, f := range files {
		data, err := os.ReadFile(f.AbsPath)
		if err != nil {
			return "", fmt.Errorf("reading %s: %w", f.AbsPath, err)
		}
		encoded, err := embed.Encode(data)
		if err != nil {
			return "", fmt.Errorf("encoding %s: %w", f.RelPath, err)
		}
		fmt.Fprintf(&b, "  [\"%s\"]=\"%s\"\n", f.RelPath, encoded)
	}
	b.WriteString(")\n")

	b.WriteString(`
bashfs_cat() {
  local _d="${__bashfs_data[$1]}"
  if [ -z "$_d" ]; then
    echo "bashfs: file not found: $1" >&2
    return 1
  fi
  printf '%s' "$_d" | base64 -d | gzip -d
}

bashfs_extract() {
  local _d="${__bashfs_data[$1]}"
  if [ -z "$_d" ]; then
    echo "bashfs: file not found: $1" >&2
    return 1
  fi
  mkdir -p "$(dirname "$2")" && printf '%s' "$_d" | base64 -d | gzip -d > "$2"
}

`)

	b.WriteString("bashfs_list() {\n")
	for _, f := range files {
		fmt.Fprintf(&b, "  echo '%s'\n", f.RelPath)
	}
	b.WriteString("}\n")

	b.WriteString("\nbashfs_jq() { bashfs_cat \"$1\" | jq \"${2:-.}\"; }\n")

	return b.String(), nil
}
